package com.mycompany.java1.fa25.mob1014_01.lab3;

public class WhileDemo {

    public static void main(String[] args) {
        System.out.println("While Loop Examples");

        // while ...
        int i = 0;
        while (i < 5) {
            System.out.println(i);

            i++;
        }

        // do ... while
        int j = 0;
        do {
            System.out.println(j);
            j++;
        } while (j < 5);
    }

}
